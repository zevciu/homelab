# ==============================================================================
# METADATA
# ==============================================================================
# FUNCTION: confirm_action
# PURPOSE: Prompt user for confirmation before executing potentially destructive actions
#
# DEPENDENCIES:
#   - logger.sh (log_warn)
# ==============================================================================

# -----------------------------------------------------------------------------
# INPUT / OUTPUT CONTRACT
# -----------------------------------------------------------------------------
# INPUT:
#   $1 - msg: confirmation message to display
#   $CONFIRM: if "true", skip confirmation and return success (global flag)
#
# OUTPUT:
#   stdout: none (reads from stdin)
#
# SIDE EFFECTS:
#   - Reads user input from terminal
#   - Logs warning if action is aborted
#
# RETURNS:
#   0 on success (user confirmed or CONFIRM=true)
#   1 on failure (user declined)
# -----------------------------------------------------------------------------

# ==============================================================================
# CONFIGURATION (none)
# ==============================================================================

# ==============================================================================
# IMPLEMENTATION
# ==============================================================================

confirm_action() {
    # ---------------------------------------------------------------------------
    # INPUT
    # ---------------------------------------------------------------------------
    local msg="$1"

    # ---------------------------------------------------------------------------
    # VALIDATION
    # ---------------------------------------------------------------------------
    [ -z "$msg" ] && die "confirm_action: message is required"

    # ---------------------------------------------------------------------------
    # SETUP
    # ---------------------------------------------------------------------------
    local answer

    # ---------------------------------------------------------------------------
    # CORE
    # ---------------------------------------------------------------------------

    # --- skip confirmation if global flag is set ---
    if [ "${CONFIRM:-false}" = true ]; then
        return 0
    fi

    # --- prompt user for confirmation ---
    read -rp "$msg [y/N]: " answer

    # ---------------------------------------------------------------------------
    # OUTPUT / RETURN
    # ---------------------------------------------------------------------------

    # --- check user response ---
    case "$answer" in
        y|Y)
            return 0
            ;;
        *)
            log_warn "aborted"
            return 1
            ;;
    esac
}

