# ==============================================================================
# METADATA
# ==============================================================================
# FUNCTION: parse_global_flags
# PURPOSE: Parse global command-line flags and set environment variables
#
# DEPENDENCIES:
#   - None (self-contained)
# ==============================================================================

# -----------------------------------------------------------------------------
# INPUT / OUTPUT CONTRACT
# -----------------------------------------------------------------------------
# INPUT:
#   $@ - command-line arguments to parse
#   DRY_RUN: initial value (default: false)
#   CONFIRM: initial value (default: false)
#   PARSED_ARGS: initial value (default: empty array)
#
# OUTPUT:
#   stdout: none
#
# SIDE EFFECTS:
#   - Sets DRY_RUN, CONFIRM, and PARSED_ARGS global variables
#   - Exports DRY_RUN and CONFIRM for child processes
#
# RETURNS:
#   0 on success
# -----------------------------------------------------------------------------

# ==============================================================================
# CONFIGURATION
# ==============================================================================
: "${DRY_RUN:=false}"
: "${CONFIRM:=false}"
: "${PARSED_ARGS:=()}"

# ==============================================================================
# IMPLEMENTATION
# ==============================================================================

parse_global_flags() {
    # ---------------------------------------------------------------------------
    # INPUT (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # VALIDATION (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # SETUP
    # ---------------------------------------------------------------------------
    local args=()

    # ---------------------------------------------------------------------------
    # CORE
    # ---------------------------------------------------------------------------

    # --- iterate through all arguments ---
    while [[ $# -gt 0 ]]; do
        case "$1" in
            --dry-run)
                DRY_RUN=true
                shift
                ;;
            --yes)
                CONFIRM=true
                shift
                ;;
            *)
                args+=("$1")
                shift
                ;;
        esac
    done

    # --- export flags for child processes ---
    export DRY_RUN
    export CONFIRM

    # --- store remaining arguments ---
    PARSED_ARGS=("${args[@]}")

    # ---------------------------------------------------------------------------
    # OUTPUT / RETURN
    # ---------------------------------------------------------------------------
    return 0
}
