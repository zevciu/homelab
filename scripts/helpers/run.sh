# ==============================================================================
# METADATA
# ==============================================================================
# FUNCTION: run
# PURPOSE: Execute a command or simulate execution in dry-run mode
#
# DEPENDENCIES:
#   - logger.sh (log_warn)
#   - global_flags.sh (DRY_RUN)
# ==============================================================================

# -----------------------------------------------------------------------------
# INPUT / OUTPUT CONTRACT
# -----------------------------------------------------------------------------
# INPUT:
#   $@ - command and arguments to execute
#   DRY_RUN: if "true", simulate execution without running the command
#
# OUTPUT:
#   stdout: command output (if not dry-run) or dry-run message
#
# SIDE EFFECTS:
#   - Executes the provided command (unless dry-run)
#   - Logs warning in dry-run mode
#
# RETURNS:
#   non-zero if command fails (unless dry-run)
# -----------------------------------------------------------------------------

# ==============================================================================
# CONFIGURATION (none)
# ==============================================================================

# ==============================================================================
# IMPLEMENTATION
# ==============================================================================

run() {
    # ---------------------------------------------------------------------------
    # INPUT (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # VALIDATION (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # SETUP (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # CORE
    # ---------------------------------------------------------------------------

    # --- check if dry-run mode is active ---
    if [ "${DRY_RUN:-false}" = true ]; then
        log_warn "DRY-RUN mode (no action will be taken)"
        echo "[DRY-RUN] $*"
        echo
    else
        # --- execute the command ---
        "$@"
    fi

    # ---------------------------------------------------------------------------
    # OUTPUT / RETURN (none)
    # ---------------------------------------------------------------------------
}
