# ==============================================================================
# METADATA
# ==============================================================================
# FUNCTION: stop_containers
# PURPOSE: Stop running Docker containers for the project
#
# DEPENDENCIES:
#   - logger.sh (log, log_err)
#   - run.sh (run)
#   - get_runtime_state.sh (get_runtime_state)
# ==============================================================================

# -----------------------------------------------------------------------------
# INPUT / OUTPUT CONTRACT
# -----------------------------------------------------------------------------
# INPUT:
#   $BOOTSTRAP_COMPOSE: path to docker-compose.bootstrap.yml (required env var)
#
# OUTPUT:
#   stdout: log messages indicating progress
#
# SIDE EFFECTS:
#   - Stops Docker containers
#
# RETURNS:
#   0 on success
#   0 if already stopped (no action taken)
# -----------------------------------------------------------------------------

# ==============================================================================
# CONFIGURATION (none)
# ==============================================================================

# ==============================================================================
# IMPLEMENTATION
# ==============================================================================

stop_containers() {
    # ---------------------------------------------------------------------------
    # INPUT (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # VALIDATION (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # SETUP
    # ---------------------------------------------------------------------------

    # --- get current runtime state ---
    local runtime_state
    runtime_state=$(get_runtime_state)

    # ---------------------------------------------------------------------------
    # CORE
    # ---------------------------------------------------------------------------

    # --- check if containers are already stopped ---
    if [ "$runtime_state" = "stopped" ]; then
        log_err "no running containers → skip stop"
        return 0
    fi

    # --- stop containers ---
    log "stopping containers..."
    run docker compose -f "$BOOTSTRAP_COMPOSE" down

    # ---------------------------------------------------------------------------
    # OUTPUT / RETURN
    # ---------------------------------------------------------------------------
    return 0
}
