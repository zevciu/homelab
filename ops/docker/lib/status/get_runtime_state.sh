# ==============================================================================
# METADATA
# ==============================================================================
# FUNCTION: get_runtime_state
# PURPOSE: Check if Docker containers are running
#
# DEPENDENCIES:
#   - None (uses docker compose)
# ==============================================================================

# -----------------------------------------------------------------------------
# INPUT / OUTPUT CONTRACT
# -----------------------------------------------------------------------------
# INPUT:
#   $DOCKER_COMPOSE: path to docker-compose.yml (required env var)
#
# OUTPUT:
#   stdout: "running" or "stopped"
#
# SIDE EFFECTS:
#   - None (read-only)
#
# RETURNS:
#   0 on success
# -----------------------------------------------------------------------------

# ==============================================================================
# CONFIGURATION (none)
# ==============================================================================

# ==============================================================================
# IMPLEMENTATION
# ==============================================================================

get_runtime_state() {
    # ---------------------------------------------------------------------------
    # INPUT (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # VALIDATION (none)
    # ---------------------------------------------------------------------------

    # ---------------------------------------------------------------------------
    # SETUP
    # ---------------------------------------------------------------------------
    local running

    # ---------------------------------------------------------------------------
    # CORE
    # ---------------------------------------------------------------------------

    # --- check running containers ---
    running=$(docker compose -f "$DOCKER_COMPOSE" ps --status running -q 2>/dev/null || true)

    # ---------------------------------------------------------------------------
    # OUTPUT / RETURN
    # ---------------------------------------------------------------------------

    # --- determine state ---
    if [ -n "$running" ]; then
        echo "running"
    else
        echo "stopped"
    fi

    return 0
}
